//Defining objects using object literal
// The object literal method of creating objects starts with a standard variable  definition, using the let keyword, followed by the assignment operator:
// In the right side of the statement, however, you’ll use curly braces with comma‐separated name/value pairs
let information ={
  name:"Priyanshu",
  class : 12,
  address :"31/26/04, Jaipur"
}
console.log(information);
console.log(information.name);
